public class Calculations {
    public double sum (double n1, double n2) {
        return n1+n2;
    }
    public double difference (double n1, double n2) {
        return n1-n2;
    }
    public double multiply (double n1, double n2) {
        return n1*n2;
    }
    public double divide (double n1, double n2) {
        return n1/n2;
    }
}
